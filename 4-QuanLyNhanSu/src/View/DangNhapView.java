    private void btnDangNhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangNhapActionPerformed
        String taiKhoan = txtTaiKhoan.getText();
        String matKhau = String.valueOf(txtPassword.getPassword());
        boolean isOk = true;
        if (taiKhoan.trim().isEmpty() || matKhau.trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, 
                    "Tài khoản hoặc Mật khẩu không được trống!");
            txtTaiKhoan.setText("");
            txtPassword.setText("");
            isOk = false;
        }
        if (isOk) {
            boolean isLogged = false;
            if (taiKhoan.equals("admin") && matKhau.equals("123")) {
                TrangChuView trangChu = new TrangChuView();
                trangChu.setVisible(true);
                this.dispose();
                isLogged = true;
            }
            if (!isLogged) {
                JOptionPane.showMessageDialog(rootPane, "Tài khoản hoặc mật khẩu sai! "
                        + "Vui lòng nhập Tài khoản: admin, Mật khẩu: 123");
                txtTaiKhoan.setText("");
                txtPassword.setText("");
            }
        }
    }//GEN-LAST:event_btnDangNhapActionPerformed

